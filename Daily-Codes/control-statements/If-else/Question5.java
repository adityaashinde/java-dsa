 // Take an Integer as input and print whether it is divisible by 4 or not 
 // Input : 5
 // Output : Not Divisible
 
class Question5 {
	public static void main (String[] args){

		int x = 5;

		if (x % 4 == 0){
		System.out.println("Divisible by 4");
		}
		else {
		System.out.println("Not Divisible");
		}
	}
}
