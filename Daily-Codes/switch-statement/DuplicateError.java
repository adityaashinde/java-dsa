class DuplicateError{
	public static void main(String []args){

		int ch = 65;
		switch(ch){
		
			case 'A':
				System.out.println("Char-A");
				break;

			case 65 :
				System.out.println("Num-65");
				break;

			case 'B':
				System.out.println("Char-B");
				break;

			case 66 :
				System.out.println("Num-66");
				break;
		}
	}
}
